# Public: Takes two Integer as input and add them together as an output.
#
# term1  - The first Integer to be added.
# term2  - The second Integer to be added.
# 
#
# Examples
#
#   add(4, 2)
#   # => '6'
#
# Returns the two Integer added together.
def add(term1, term2)
    return term1 + term2
end
